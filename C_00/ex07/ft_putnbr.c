#include <unistd.h>

void	my_print(int a)
{
	a = a + 48;
	write (1, &a, 1);
}

void	loop(int b)
{
	if (b >= 10)
	{
		loop(b / 10);
	}
	my_print(b % 10);
}

void	ft_putnbr(int nb)
{
	if (nb < 0 && nb != -2147483648)
	{
		nb = -nb;
		write (1, "-", 1);
	}
	if (nb != -2147483648)
	{
		loop(nb);
	}
	if (nb == -2147483648)
	{
		write (1, "-2147483648", 11);
	}
}
